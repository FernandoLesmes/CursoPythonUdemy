letra = "u"

if letra.lower() == "a":
    print ("Esta vocal es la A")
elif letra.lower() == "e":
    print("Eta vocal es la E")
elif letra.lower() == "i":
    print("Esta vocal es la I ")
elif letra.lower() == "o":
    print ("Esta vocal es la o")
else:
    print("Esta vocal es la U")
